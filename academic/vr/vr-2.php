						<p>
之前已经和大家分享了一些有关VR的干货，但是只是看着这些产品头盔完全无法想象VR到底是个什么玩意儿对不对！So，我们特意联系了国内虚拟现实行业领先企业上海举佳爽网络科技(Famiku)有限公司，带领大家深入VR战场一线，感受虚拟现实的世界！大家准备好了嘛？（友情提示：前方多图预警，务必及时连接wifi！）
</p>
<p class="part">
设备篇
</p>
<p class="part tl">
基本设备：
</p>
<p>
	VR头盔、动作捕捉装置or游戏手柄、高性能主机、显示屏（非必需，供旁人感受游戏者的世界）↓
</p>
<div class="image"><img src="<?php echo SUBROOT;?>./imgs/academic/vr/vr-2/1.png"></div>
<p>
眼下VR设备的工作方式其实很好理解，说白了其实就是将两片小屏幕放在距离眼睛很近的距离上，并将原来鼠标移动镜头的功能置换到了头戴设备中，通过陀螺仪、追踪器、电磁感应装置等内置在头盔内的设备实现这个曾经简单的功能。
</p>
<p>
理论上讲，单靠上述装置就应该能够实现对使用者头部动作的捕捉，但由于目前的技术还不够成熟，三家大的VR硬件开发者都还要仰赖额外的外部装置来实现精确捕捉，即外部摄像头或LED传感器，或是使用游戏手柄。
</p>
<div class="image"><img src="<?php echo SUBROOT;?>./imgs/academic/vr/vr-2/2.png"></div>
<p class="image-title">VR头盔就仿佛是一扇任意门，近距离看一眼头盔的内部，是不是觉得里面藏着另外一个世界呢？</p>
