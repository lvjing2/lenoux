<p>同时，较大的压迫感也使部分体验者在进行VR体验的过程中时不时得抽出一只手扶住头盔。</p>
<p>而佩戴眼镜的小伙伴们沮丧的发现，拿下头盔后，眼镜君任性滴在鼻翼两侧留下了两点红印，抗议之前受到的压迫。</p>
<p>再回头看之前打僵尸的小兄弟，脱下眼镜，满头大汗。。小编不禁略感庆幸，还好体验的不是自己，不然，再精致的妆容可也经不起这番折腾呀！</p>
<p>游戏体验  </p>
<p>沉浸指数：☆☆☆☆   </p>
<p>危险指数：☆☆   </p>
<p>眩晕指数：☆☆☆</p>
<div class="image"><img src="<?php echo SUBROOT;?>./imgs/academic/vr/vr-2/12.png"></div>
<p>仔细的小伙伴或许会发现，我们在使用头盔时，身后可以连着一条长长的“尾巴”，而沉浸在VR世界的我们并无法看到现实世界周边的环境，因此还是有一定可能被这“尾巴”绊倒或是装上周边其他的物体，危险系数不容忽视！</p>
<p>对于玩3D游戏都会晕的小编来说，VR体验最大的问题就在于晕眩感。而当前的VR体验是没法让体验者一玩几十分钟的，主要原因在于 VR 头显的性能无法保证用户长时间佩戴而不产生晕眩感，据说目前能撑过半小时还不晕的人寥寥无几。。。</p>
<p>围观者体验</p>
<p>搞笑指数：☆☆☆☆  </p>
<p>体验者沉浸在自己的世界里，High的飞起，然而一旁的围观者看着他就觉得。。。有点。。傻。。。</p>
<p class="part">
	结语
</p>
<p>总体来说，相比PC上的游戏，带着VR头盔，玩家将更具有沉浸感，更容易投入游戏。但若VR真要走进家家户户，那还是需要强大的游戏PC支撑，且其对接口数量也有一定要求，同时也需解决头盔沉重、移动范围限制、晕眩感等各类用户体验问题。目前的VR体验更多是通过视觉方面，而多通道整合对用户的体验是十分重要的，若能将视觉、触觉、嗅觉等多感觉通道联合，想必才是正真的虚拟现实吧~然而。。我们还有很长的路要走。。。</p>


