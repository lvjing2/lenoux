<p>头盔佩戴体验</p>
<p>舒适指数：☆☆</p>
<div class="image"><img src="<?php echo SUBROOT;?>./imgs/academic/vr/vr-2/9.png"></div>
<p>
	VR头盔虽然看起来炫酷，戴起来却不那么容易，且对于头可断，血可流，发型不能乱的爱美人士来说可是一大考验，由于头盔自重较沉，需要第三根头戴支撑，由此就可能变成。。。这样↓
</p>
<div class="image"><img src="<?php echo SUBROOT;?>./imgs/academic/vr/vr-2/10.png"></div>
<p>亦或是。。这样↓</p>
