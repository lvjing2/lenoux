决定整个行业的格局，可现在冥昭瞢闇，谁主沉浮尚无定论。
</p>
<p class="part">
FUTURE</p>
<p>
虽然VR的相关产品已遍地开花，但这并不能认为VR已经可以走进人们的生活，一如当初的智能手
机，VR领域是一个完完整整全新的蓝海产业，相对手机而言，VR的生态链将会更长更广更深。系
统平台、现实设备、输入输出、内容制作、应用开发、游戏音乐、影视娱乐、传统结合、云服务、
个性化等等。。
</p>
<p class="part tl">
用户体验
</p>
<p>
VR也不像手机一样是单个的设备，想要拥有完美并具有沉浸感的VR体验需要一整套的设备集群
（VR头盔、耳机、动作捕捉设备、空间定位装置、360度全景相机、360度音频、反馈装置等等等
等等）。然而这些设备大都处于研发的初期阶段，需要解决的问题都非常多。
</p>
<p>
即使不需要那么重度的体验，仅使用VR头显，也有显示屏材质、分辨率、延迟、PPI、帧速率等很
多问题要解决。VR头盔采用光学透镜放大屏幕上的内容，分辨率的高低将决定用户看到画面的清晰
度，2K是基本的需求，单目前是这大部分设备未能达到的。如果不想让VR用户产生眩晕感，那显示
屏的fps至少需要提高到90+,延时小于
20ms，而若要给用户带来极致的体验，还需配上输入设备，所以便宜好用的动作捕捉设备也成了
现在全世界的研发方向之一。
</p>
<p class="part tl">
内容制作
</p>
<p>
另一大VR发展的问题则主要聚焦在VR的内容，去年由于尚未出现真正意义的VR平台以及硬件设备，
导致内容制作公司动力不足持续观望，大多只推出体验内容，使得VR内容发展的势头被削弱。但
今年正式进入黄金年代，万事俱备，东风已来，内容在不久的将来将不再是一个问题，而是一片
广阔的蓝海。
</p>
<p>
且不说VR在游戏方面的广阔前景，前段时间，电影界大佬张艺谋放话，将虚拟现实技术应有在娱乐
领域中是产业发展趋势，VR的出现让电影更有趣，更注重体验者之间的互动，能带来更加震撼和身
临其境的体验效果。由此可见，比起VR游戏，影视的风口或许更受期待。
</p>
<p>
游戏算是VR内容中最常见也是最成熟的部分，影视是未来看好的风口，但制作起来有难度，需要
时间来进行缓冲。因此，有人就把目光投向了直播，通过体育赛事、演唱会直播来加速虚拟现实
落地，这种关注度极高的活动必然会得到用户的青睐。直播技术要求不高，而用户也喜欢这种贴
近距离的视觉享受，因此VR直播也可能成为VR大潮中的一股清流。
</p>
<p class="part tl">
 “结语”
</p>
<p>
虽然VR领域还有很多未知问题亟待解决，但风险必然与机遇并存，每次平台的变更，影响的用户
量都将是上一代的百倍。二十一世纪初智能手机改变了世界，下一个十年VR能否成为革命性的计
算平台之一还是未知。如果想到几十年后，伴随在你身边的主流设备依然只有电视、PC、手机、
平板，那这个世界必然会显得无趣很多。。。而VR如同潘多拉的魔盒，与物质和思维完全捆绑在
一起，一旦被开启，对整个社会将产生巨大的影响。无论如何，VR所造就的另一个世界都将值得
我们为之期待，为之着迷。
</p>

