						<p class="title">VR,到底是个什么鬼？(上)</p>
						<p>
							<span class="des-name">By DUNDUN Ideapad用户研究中心</span> |
							<span class="des-time">2016-04-18</span>
						</p>
						<p>
我们经常听到别人提起VR，AR，MR，然而这些R子。。都是谁呢？
其实，他们都是英文单词的缩写~
</p>
<p>
VR即Virtual Reality也就是虚拟现实，虚拟现实技术是一种可以创建和体验虚拟世界的计算机仿真
系统它利用计算机生成一种模拟环境是一种多源信息融合的交互式的三维动态视景和实体行为的
系统仿真使用户沉浸到该环境中。
</p>
<p>
AR则是增强现实技术(Augmented Reality)，是一种实时地计算摄影机影像的位置及角度并加上相
应图像的技术，这种技术的目标是在屏幕上把虚拟世界套在现实世界并进行互动。
</p>
<p>
MR是混合现实Mixed Reality的英文缩写，混合现实技术是虚拟现实技术的进一步发展，该技术通
过在虚拟环境中引入现实场景信息，在虚拟世界、现实世界和用户之间搭起一个交互反馈的信息
回路，以增强用户体验的真实感。
</p>
<p>
VR、AR、MR可以说是一个比一个高级，所以今天就让我们先走近VR，一起看看VR的过去、现在
与将来吧~
</p>
<p class="part">
PAST
</p>
<p>
虚拟现实这项黑科技早的起源甚至可以追溯至现代计算机技术的诞生，但与当代设备相仿的第一个
原型机是著名计算机科学家、图灵奖获得者伊万·萨瑟兰(Ivan Sutherland)发明的头戴式显示器。
1968年，作为哈弗大学电气工程副教授的萨瑟兰发明了↓
</p>
<div class="image"><img src="<?php echo SUBROOT;?>./imgs/academic/vr/vr-1/vr-1.png"></div>
<p>
这样的一台头戴式显示设备，并给他取了一个狂拽炫酷嗲炸天的名字——
The Sword of Damocles（达摩克利斯之剑）
</p>
<p>
呃。。。好吧。。人家说是剑就是剑吧。。
整套系统将显示设备放置在用户头顶的天花板，并通过连接杆和头戴设备相连，能够将简单线框图
转换为3D效果的图像。
</p>
<p>
那为什么说达摩克利斯之剑是第一个虚拟现实的原型设备呢？因为它定义了虚拟现实的几个要素：
        立体显示：
          原型使用了两个1吋的CRT显示器分别显示不同视角的图像，来创造立体视觉。
       虚拟画面生成：
          视频中的立方体是实时计算渲染出来的。
       头部位置跟踪：
          原型使用了两种方式来对头部位置进行跟踪，机械连杆和超声波检测。你没有看错，在1968
          年，这个看似简陋的原型有三个超声波发生器和四个接收器，用来跟踪头部运动。
       虚拟环境互动：
          这一环节上，达摩克利斯之剑就显得对不起自己酷炫的名字了。因为唯一和这台设备互动的
          手段，是双手操作的把手。
       模型生成：
          虽然这只是个简单的立方体，仅有8个顶点，但的确是通过空间坐标建立的模型。如果你观
          察足够仔细的话，应该可以看到，视频中这个简单的立方体是随着人的视角在变化的。
</p>
<p>
不得不承认，以现代的眼光来看达摩克利斯之剑，的确。。有些简陋，但是考虑到这是1968年的
原型，你不得不赞叹这个超越时代的作品。不难想象，在那个连鼠标都还没被发明的时代，这样
“疯狂”的构想，也就只能放在实验室里落灰了。事实上也是如此，伊万·萨瑟兰的论文在1968年秋
季发表后影响并不大，虚拟现实也并没有在那个时代成为话题。
</p>
<p class="part">
 PRESENT
</p>
<p>
经过了长达半个世纪的酝酿，如今VR的这坛美酒已经到了要开封飘香的时候了~从三星发布Gear 
VR虚拟现实装置到HTC宣布Vive，从索尼展示PS4虚拟现实头戴设备到Facebook发售Oculus Rift
虚拟现实头盔，可以说，不到半年时间，几大知名科技巨头已经把虚拟现实VR设备推到大众面前，
在圈内掀起了VR的高潮。转眼国内的VR领域，初创公司如雨后春笋，爆发在即，所以，2016年也
被称为虚拟元年~
</p>
