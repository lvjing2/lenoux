							六、哪些能力最重要？
						</p>
						<p>
							调查分析发现，为了保证核心竞争力，用户体验行业的从业者需要具备的通用能力有“沟通”、“需求理解”、“团队合作”、“用户体验思维”、“逻辑分析”等，但不同类别的用户体验从业者能力要求的侧重点有所差异。
